let fruits= ["apple","mango"];
console.log(fruits);

//push and pop
fruits.push("melon");///it will add element in the last of array
console.log(fruits);

fruits.pop();//it will delete the element from the last index
console.log(fruits);

//shift and unshift 

fruits.shift();//it means first element shiftted out 
console.log(fruits);

fruits.unshift("kiwi");//it is added the element to the first place
console.log(fruits);